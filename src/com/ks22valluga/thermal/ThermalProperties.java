package com.ks22valluga.thermal;

public class ThermalProperties {
	private float mass;
	private float temp;
	private float parentConductance;
	public float getMass() {
		return mass;
	}
	public void setMass(float mass) {
		this.mass = mass;
	}
	public float getTemp() {
		return temp;
	}
	public void setTemp(float temp) {
		this.temp = temp;
	}
	public float getParentConductance() {
		return parentConductance;
	}
	public void setParentConductance(float parentConductance) {
		this.parentConductance = parentConductance;
	}

}
